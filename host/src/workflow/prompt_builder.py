"""
prompt_builder.py
==================
LLMへのプロンプトテンプレートと出力フォーマットを定義。

★ ここを書き換えることでエージェントへの指示内容を変更できます ★

変更できること:
  - DEFAULT_OUTPUT_FORMAT : 各エージェントの発言フォーマット
  - AGENT_PROMPT_TEMPLATE : エージェント発言プロンプト全体
  - SUMMARY_PROMPT_TEMPLATE : テーマ要約プロンプト

注意:
  AGENT_PROMPT_TEMPLATE の変数プレースホルダー:
    {role}, {task}, {name}, {query}, {pre_info_section}, {stance_section}, {rag_section}, {history}, {output_format}
  SUMMARY_PROMPT_TEMPLATE の変数プレースホルダー:
    {theme}, {history}, {output_format}
"""

# -------------------------------------------------------------------
# 発言フォーマット
# {name} はペルソナ名に自動で置換されます
# -------------------------------------------------------------------
DEFAULT_OUTPUT_FORMAT = (
    "【発言者】{name}\n"
    "【主張】(1〜2文で主張を述べる)\n"
    "【根拠】(根拠や補足を1〜2文で)\n"
    "300字以内で記述してください。"
)

# -------------------------------------------------------------------
# エージェント発言プロンプト
# -------------------------------------------------------------------
AGENT_PROMPT_TEMPLATE = """\
あなたは {role} です。
タスク: {task}
あなたの名前は {name} です。グループディスカッションに参加しています。

議題 (Query): {query}

{pre_info_section}

{stance_section}

{rag_section}

直近の会話履歴:
{history}

他テーマの要約:
{previous_summaries}

以下のフォーマットで発言してください:
{output_format}
"""

# -------------------------------------------------------------------
# テーマ要約プロンプト
# -------------------------------------------------------------------
SUMMARY_PROMPT_TEMPLATE = """\
テーマ「{theme}」に関するディスカッションを要約してください。
各ペルソナが主張したポイントを整理してまとめてください。

ディスカッション履歴:
{history}

以下のフォーマットで発言してください:
{output_format}
"""

# -------------------------------------------------------------------
# 評価プロンプト（階層型ストラテジー用）
# マネージャーがワーカーの発言を評価し、合否をJSON形式で返す
# -------------------------------------------------------------------
EVALUATION_PROMPT_TEMPLATE = """\

--- ワーカーの発言一覧 ---
{worker_opinions}
---

あなたはマネージャーとして、上記のワーカーたちの発言を評価してください。{pass_condition_section}

以下のJSON形式のみで回答してください（前後の説明は不要です）:
{{"pass": true, "complete": true, "feedback": "合格理由（日本語）"}}
または
{{"pass": false, "complete": false, "feedback": "不合格理由と具体的な改善点（日本語）"}}
"""

# -------------------------------------------------------------------
# 批判プロンプト（敵対的ストラテジー用）
# 批判役が生成役の提案に対してダメ出しを行う
# -------------------------------------------------------------------
CRITIC_PROMPT_TEMPLATE = """\

--- 生成役の提案 ---
{generated_content}
---

{critic_perspective_section}上記の提案に対して批判的な観点から評価してください。
具体的な問題点・リスク・見落とし・改善すべき点を指摘してください。
"""

# -------------------------------------------------------------------
# 批判役評価プロンプト（敵対的ストラテジー用 - JSON評価版）
# 批判役が生成役の提案を評価し、修正が必要か JSON で判定する
# -------------------------------------------------------------------
CRITIC_EVALUATION_PROMPT_TEMPLATE = """\

--- 生成役の提案 ---
{generated_content}
---

{critic_perspective_section}上記の提案に対して批判的な観点から評価してください。
具体的な問題点・リスク・見落とし・改善すべき点を指摘してください。

評価後、以下のJSON形式のみで回答してください（前後の説明は不要です）:
修正が必要な場合:
{{"pass": false, "complete": false, "feedback": "具体的な問題点と改善点（日本語）"}}
問題なし・修正不要の場合:
{{"pass": true, "complete": true, "feedback": "合格理由（日本語）"}}
"""

# -------------------------------------------------------------------
# 裁判官プロンプト（Judge & Jury ストラテジー用）
# 裁判官がディベートの全履歴を読み、最終判定を下す
# -------------------------------------------------------------------
JUDGE_PROMPT_TEMPLATE = """\

--- これまでのディベート ---
{debate_history}
---

{evaluation_criteria_section}あなたは裁判官として、上記のディベートを総合的に評価し、最終判定を下してください。
各参加者の論点を整理し、どの立場・主張が最も説得力があるかを判断して結論を述べてください。

最後に、以下のJSON形式で判定結果を出力してください:
{{"pass": true, "complete": true, "feedback": "最終判定の要約（日本語）"}}
"""

