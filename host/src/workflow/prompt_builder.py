"""
prompt_builder.py
==================
LLMへのプロンプトテンプレートと出力フォーマットを定義。

★ ここを書き換えることでエージェントへの指示内容を変更できます ★

変更できること:
  - DEFAULT_OUTPUT_FORMAT : 各エージェントの発言フォーマット
  - AGENT_PROMPT_TEMPLATE : エージェント発言プロンプト全体
  - SUMMARY_PROMPT_TEMPLATE : テーマ要約プロンプト

注意:
  AGENT_PROMPT_TEMPLATE の変数プレースホルダー:
    {role}, {task}, {name}, {query}, {pre_info_section}, {rag_section}, {history}, {output_format}
  SUMMARY_PROMPT_TEMPLATE の変数プレースホルダー:
    {theme}, {history}, {output_format}
"""

# -------------------------------------------------------------------
# 発言フォーマット
# {name} はペルソナ名に自動で置換されます
# -------------------------------------------------------------------
DEFAULT_OUTPUT_FORMAT = (
    "【発言者】{name}\n"
    "【主張】(1〜2文で主張を述べる)\n"
    "【根拠】(根拠や補足を1〜2文で)\n"
    "300字以内で記述してください。"
)

# -------------------------------------------------------------------
# エージェント発言プロンプト
# -------------------------------------------------------------------
AGENT_PROMPT_TEMPLATE = """\
あなたは {role} です。
タスク: {task}
あなたの名前は {name} です。グループディスカッションに参加しています。

議題 (Query): {query}

{pre_info_section}

{rag_section}

直近の会話履歴:
{history}

他テーマの要約:
{previous_summaries}

以下のフォーマットで発言してください:
{output_format}
"""

# -------------------------------------------------------------------
# テーマ要約プロンプト
# -------------------------------------------------------------------
SUMMARY_PROMPT_TEMPLATE = """\
テーマ「{theme}」に関するディスカッションを要約してください。
各ペルソナが主張したポイントを整理してまとめてください。

ディスカッション履歴:
{history}

以下のフォーマットで発言してください:
{output_format}
"""

# -------------------------------------------------------------------
# 評価プロンプト（階層型ストラテジー用）
# マネージャーがワーカーの発言を評価し、合否をJSON形式で返す
# -------------------------------------------------------------------
EVALUATION_PROMPT_TEMPLATE = """\

--- ワーカーの発言一覧 ---
{worker_opinions}
---

あなたはマネージャーとして、上記のワーカーたちの発言を評価してください。{pass_condition_section}

以下のJSON形式のみで回答してください（前後の説明は不要です）:
{{"pass": true, "feedback": "合格理由（日本語）"}}
または
{{"pass": false, "feedback": "不合格理由と具体的な改善点（日本語）"}}
"""

# -------------------------------------------------------------------
# 批判プロンプト（敵対的ストラテジー用）
# 批判役が生成役の提案に対してダメ出しを行う
# -------------------------------------------------------------------
CRITIC_PROMPT_TEMPLATE = """\

--- 生成役の提案 ---
{generated_content}
---

{critic_perspective_section}上記の提案に対して批判的な観点から評価してください。
具体的な問題点・リスク・見落とし・改善すべき点を指摘してください。
"""

# -------------------------------------------------------------------
# 裁判官プロンプト（Judge & Jury ストラテジー用）
# 裁判官がディベートの全履歴を読み、最終判定を下す
# -------------------------------------------------------------------
JUDGE_PROMPT_TEMPLATE = """\

--- これまでのディベート ---
{debate_history}
---

{evaluation_criteria_section}あなたは裁判官として、上記のディベートを総合的に評価し、最終判定を下してください。
各参加者の論点を整理し、どの立場・主張が最も説得力があるかを判断して結論を述べてください。
"""

# -------------------------------------------------------------------
# ゲートキーパー評価プロンプト（ステージゲートフロー用）
# ゲートキーパーがテーマ結果を評価し、次フェーズへ進む品質か判定する
# -------------------------------------------------------------------
GATEKEEPER_PROMPT_TEMPLATE = """\

--- テーマ「{theme}」の実行結果 ---
{summary}
---

{pass_condition_section}ゲートキーパーとして、上記のテーマ実行結果を評価してください。
次のフェーズに進む品質基準を満たしていますか？

以下の JSON 形式のみで回答してください（前後の説明は不要です）:
{{"pass": true, "feedback": "通過理由（日本語）"}}
または
{{"pass": false, "feedback": "不合格理由と具体的な改善点（日本語）"}}
"""

# -------------------------------------------------------------------
# スプリント完成判定プロンプト（アジャイルスプリントフロー用）
# スプリント完了後に完成判定者が全体の仕上がりを評価する
# -------------------------------------------------------------------
SPRINT_COMPLETION_PROMPT_TEMPLATE = """\

--- これまでのスプリント結果 ---
{sprint_summaries}
---

{completion_criteria_section}プロジェクト全体のスプリント結果を踏まえて、作業は完成しましたか？

以下の JSON 形式のみで回答してください（前後の説明は不要です）:
{{"done": true, "reason": "完成と判断した理由（日本語）"}}
または
{{"done": false, "reason": "未完成の理由と追加で必要な作業（日本語）"}}
"""

# -------------------------------------------------------------------
# 条件分岐ルータープロンプト（条件分岐フロー用）
# ルーターが直前のテーマ結果から次に実行すべきテーマを選択する
# -------------------------------------------------------------------
FLOW_ROUTER_PROMPT_TEMPLATE = """\

--- 利用可能なテーマ一覧 ---
{theme_list}
---

--- 直前のテーマ「{current_theme}」の実行結果 ---
{summary}
---

{routing_rules_section}上記の結果を踏まえて、次に実行すべきテーマを選択してください。
議論を終了する場合は end: true を返してください。

以下の JSON 形式のみで回答してください（前後の説明は不要です）:
{{"next_theme_index": 番号（0始まり）, "reason": "選択理由（日本語）"}}
または
{{"end": true, "reason": "終了理由（日本語）"}}
"""
